    for index,vehicle in pairs (Vehicle.vehicleData) do
		local v = Vehicle(index)
		for indexIntern,vehicleIntern in pairs(vehicle)do
			v:add(vehicleIntern[1], vehicleIntern[2], vehicleIntern[3], vehicleIntern[4], vehicleIntern[5])
		end
    end
end)