function Vehicle:add(model,x,y,z,rotation)
	local vehicleElement = createVehicle(model, x, y, z, 0, 0, rotation)
	
	vehicleElement:setColor(255, 255, 255)
	syncVehicle(vehicleElement)
	table.insert(self.vehicles,vehicleElement)
	addEventHandler("onVehicleStartEnter", vehicleElement, function(...)
		self:onVehicleStartEnter(...)
	end)
	return vehicleElement
end

function Vehicle:init(base)
    super.init(self)
	self.vehicles = {}
	self.base = base
	self.owner = nil
	
	table.insert(Vehicle.instances,self)
	return self;
end

function Vehicle.getFromBaseName(baseName)
	if not baseName then return false end
	for i,k in pairs (Vehicle.instances) do
		if(k.base == baseName) then
			return k
		end
	end 
	return false
end

function Vehicle:getAll()
	return self.vehicles
end
function Vehicle:updateColor(r,g,b)
	for i,k in pairs(self.vehicles) do
		k:setColor(r,g,b)
	end
	return true
end

function Vehicle:onVehicleStartEnter(player)
	local team = player:getTeam()
	if self.base ~= nil then
		if not self.owner then
			outputChatBox("#FF6464[BASE]#00FF00 Este veiculo pertence a base #FF6464"..self.base, player, 255, 0, 0, true)
			cancelEvent()
			return
		end
		if not(team) or (team.name ~= self.owner) then
			outputChatBox("#FF6464[BASE]#00FF00 Este veiculo pertence a gang #FF6464"..self.owner, player, 255, 0, 0, true)
			cancelEvent()
			return
		end
	end
	setElementFrozen( source,false )
end
function Vehicle:setOwner(teamName)
	if teamName then
		self.owner = teamName
		return true
	end
	return false
end
function Vehicle:getElement()
	return self.vehicleElement
end
